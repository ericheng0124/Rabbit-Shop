// src/pages/goods/goods.vue
<script setup lang="ts">
import { getGoodsByIdAPI } from '@/services/goods'
import type { GoodsResult } from '@/types/goods'
import { onLoad } from '@dcloudio/uni-app'
import { computed, ref } from 'vue'
import AddressPanel from './components/AddressPanel.vue'
import ServicePanel from './components/ServicePanel.vue'
import type {
  SkuPopupInstanceType,
  SkuPopupLocaldata,
  SkuPopupEvent,
} from '@/components/vk-data-goods-sku-popup/vk-data-goods-sku-popup'
import { postAddCartAPI } from '@/services/cart'
import type { AddressItem } from '@/types/address'
import { getMemberAddressAPI } from '@/services/address'
import { useAddressStore } from '@/stores/modules/address'

// 获取屏幕边界到安全区域距离
// const { safeAreaInsets } = uni.getSystemInfoSync()
// const windowInfo = uni.getWindowInfo()
// const safeAreaInsets = windowInfo.safeAreaInsets
const { safeAreaInsets } = uni.getWindowInfo()
// const { safeArea } = uni.getWindowInfo()

// 接收页面参数
const query = defineProps<{
  id: string
}>()

// 获取商品详情
const goods = ref<GoodsResult>()
const getGoodsDetail = async () => {
  const res = await getGoodsByIdAPI(query.id)
  goods.value = res.result
  // 处理成sku组件所需格式
  localdata.value = {
    _id: res.result.id,
    name: res.result.name,
    goods_thumb: res.result.mainPictures[0],
    spec_list: res.result.specs.map((v) => {
      return {
        name: v.name,
        list: v.values,
      }
    }),
    sku_list: res.result.skus.map((v) => {
      return {
        _id: v.id,
        goods_id: res.result.id,
        goods_name: res.result.name,
        image: v.picture,
        price: v.price * 100, // 注意根据插件使用文档说明，价格此处需要乘以100
        sku_name_arr: v.specs.map((vv) => vv.valueName),
        stock: v.inventory,
      }
    }),
  }
}

const addressStore = useAddressStore()
// 修改发货地址
const changeAddress = (item: AddressItem) => {
  // console.log(item)
  addressStore.changeSelectedAddress(item)
}

const addressList = ref<AddressItem[]>([])
// 获取地址列表
const getAddressListData = async () => {
  const res = await getMemberAddressAPI()
  addressList.value = res.result
  // 新增：如果没有选中地址，设置默认地址
  if (!addressStore.selectedAddress) {
    const defaultAddress = res.result.find((item) => item.isDefault)
    if (defaultAddress) {
      addressStore.changeSelectedAddress(defaultAddress)
    }
  }
}

onLoad(() => {
  getGoodsDetail()
})

// 轮播图变化时的触发事件
const currentIndex = ref(0)
const onChange: UniHelper.SwiperOnChange = (event) => {
  currentIndex.value = event.detail!.current
}

// 点击轮播图变大图预览
const onTapImg = (url: string) => {
  uni.previewImage({
    current: url,
    urls: goods.value!.mainPictures,
  })
}

// 弹出层的组件ref
const popup = ref<{
  open: (type?: UniHelper.UniPopupType) => void
  close: () => void
}>()

// 弹出层条件渲染数据
const popupName = ref<'address' | 'service'>()
// 修改弹出层名称
const openPopup = (name: typeof popupName.value) => {
  popupName.value = name
  popup.value?.open()
  getAddressListData()
}

// 控制sku组件显示
const isShowSku = ref(false)

// 商品信息
const localdata = ref({} as SkuPopupLocaldata)

// 按钮模式
// 使用枚举类型
enum SkuMode {
  Both = 1,
  Cart = 2,
  Buy = 3,
}
const mode = ref<SkuMode>(SkuMode.Both)

// 打开sku弹窗修改按钮模式
const openSkuPopup = (val: SkuMode) => {
  // 显示sku组件
  isShowSku.value = true
  // 修改按钮模式
  mode.value = val
}

// sku组件实例
const skuPopupRef = ref<SkuPopupInstanceType>()
// 计算被选中的值
const selectArrText = computed(() => {
  return skuPopupRef.value?.selectArr.join(' ').trim() || '请选择商品规格'
})

// 加入购物车
const onAddCart = async (event: SkuPopupEvent) => {
  // console.log(event)
  await postAddCartAPI({ skuId: event._id, count: event.buy_num })
  uni.showToast({
    icon: 'success',
    title: '添加成功',
  })
  // 关闭弹层
  isShowSku.value = false
}

// 立即购买
const onBuyNow = (event: SkuPopupEvent) => {
  uni.navigateTo({ url: `/pagesOrder/create/create?skuId=${event._id}&count=${event.buy_num}` })
}
</script>

<template>
  <!-- SKU弹窗组件 -->
  <vk-data-goods-sku-popup
    v-model="isShowSku"
    :localdata="localdata"
    :mode="mode"
    add-cart-background-color="#FFA868"
    buy-now-background-color="#27BA9B"
    ref="skuPopupRef"
    :actived-style="{ color: '#27BA9B', borderColor: '#27BA9B', backgroundColor: '#E9F8F5' }"
    @add-cart="onAddCart"
    @buy-now="onBuyNow"
  />
  <scroll-view scroll-y class="viewport">
    <!-- 基本信息 -->
    <view class="goods">
      <!-- 商品主图 -->
      <view class="preview">
        <swiper @change="onChange" circular autoplay>
